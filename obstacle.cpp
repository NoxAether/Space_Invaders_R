Obstacle::Obstacle(Vector2 position) : _position(position) {

    for (unsigned int row = 0; row < _grid.size(); row++)
        for (unsigned int col = 0; col < _grid[0].size(); col++) {
            if (_grid[row][col] == 1) {
                float pos_x = _position.x + col * 3;
                float pos_y = position.y + row * 3;
                Block block{{pos_x, pos_y}};
                _blocks.push_back(block);
            }
        }
}

void Obstacle::Draw() {
    for (auto &block : _blocks) {
        block.Draw();
    }
}
